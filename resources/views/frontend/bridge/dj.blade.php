<ol class="mb-3 ps-3">
    <li>
        <h6>Resonance</h6> frequent ringing or buzzing
    </li>
    <li>
        <h6>attenuation</h6> To reduce the intensity of sound waves
    </li>
    <li>
        <h6>decibels</h6> It is the unit for measuring the level of sound.
    </li>
    <li>
        <h6>Microphone</h6> It is a device used to convert sound waves into electrical signals.
    </li>
    <li>
        <h6>Microphone Mixer</h6> It is a device used to combine two or more electrical signals.
    </li>
    <li>
        <h6>Amplifier</h6> It is a device used to amplify (i.e. increase power) electrical signals.
    </li>
    <li>
        <h6>Amplified Speaker</h6> This speaker has a built-in amplifier.
    </li>
    <li>
        <h6>P.A. System</h6> It is an abbreviation for Public Address System. A PA system consists of a
        microphone, mixer, amplifier, speaker, etc.
    </li>
    <li>
        <h6>Feedback</h6> It is a ringing noise that is produced when a microphone reproduces the sound
        taken from the speaker.
    </li>
    <li>
        <h6>Sound Source</h6> It can be a speaker, singer or an instrument, or any instrument that
        produces sound.
    </li>
    <li>
        <h6>Frequency Response</h6> It is in a way that a microphone-like device or a room-like
        environment responds to different frequencies.
    </li>
    <li>
        <h6>Flat Feedback</h6> This means that a device as a microphone responds in the same way to
        different frequencies i.e. whether the frequency is high or low, the device is going to respond
        in the same way. The flat response may not occur in any device or environment. However, they can
        have an almost flat response.
    </li>
    <li>
        <h6>Ear Plugs</h6> This device is used to attenuate sound waves. It is worn by people who work
        in noisy places such as clubs to prevent permanent hearing damage.
    </li>
    <li>
        <h6>Noise Reduction Rating (NRR)</h6> Any device whose NRR is less than 20 dB (decibels) is of
        no use
    </li>
</ol>
